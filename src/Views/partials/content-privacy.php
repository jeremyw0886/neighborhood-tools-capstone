  <section id="who-we-are">
    <h2>Who We Are</h2>
    <p>NeighborhoodTools (<a href="https://neighborhoodtools.org">neighborhoodtools.org</a>) is a neighborhood tool-sharing platform that connects neighbors who want to lend and borrow tools. We are based in the United States.</p>
    <p>For privacy inquiries, contact us at <a href="mailto:contact@neighborhoodtools.org">contact@neighborhoodtools.org</a>.</p>
  </section>

  <section id="information-we-collect">
    <h2>Information We Collect</h2>

    <h3>Information You Provide Directly</h3>
    <p>We collect information you voluntarily provide when you use NeighborhoodTools:</p>
    <ul>
      <li><strong>Account information</strong> &mdash; first name, last name, username, email address, and password (stored as a bcrypt hash; plaintext is never retained) during registration</li>
      <li><strong>Location information</strong> &mdash; ZIP code (required) and street address (optional) during registration and profile editing, plus neighborhood selection (auto-resolved from your address or chosen manually)</li>
      <li><strong>Profile details</strong> &mdash; bio (up to 2,000 characters), profile photo (JPEG, PNG, or WebP, max 5&nbsp;MB) with focal-point coordinates for image cropping, vector avatar selection, and contact preference (email or app)</li>
      <li><strong>Tool listings</strong> &mdash; tool name, description, category, rental fee, condition, loan duration, fuel type (when applicable), and tool photos (JPEG, PNG, or WebP, max 5&nbsp;MB)</li>
      <li><strong>Borrow requests</strong> &mdash; loan duration and optional notes to the lender</li>
      <li><strong>Borrow decisions</strong> &mdash; denial reason (required, up to 1,000 characters) and cancellation reason (optional, up to 1,000 characters)</li>
      <li><strong>Ratings and reviews</strong> &mdash; a 1&ndash;5 star score (required) and optional review text after a completed borrow</li>
      <li><strong>Incident reports</strong> &mdash; description of damage, theft, loss, or other issues, estimated damage amount, and optional photos</li>
      <li><strong>Dispute messages</strong> &mdash; text submitted during dispute resolution</li>
      <li><strong>Waiver acknowledgments</strong> &mdash; three required checkboxes accepted before borrow pickup</li>
      <li><strong>Terms of Service acceptance</strong> &mdash; recorded at registration and when new versions are published</li>
      <li><strong>Search queries</strong> &mdash; the text you enter when searching for tools</li>
    </ul>

    <h3>Information Collected Automatically</h3>
    <p>When you use NeighborhoodTools, we automatically collect:</p>
    <ul>
      <li><strong>IP address</strong> &mdash; recorded in server logs, rate-limiting systems, search logs, and Content Security Policy (CSP) violation reports for security and abuse prevention</li>
      <li><strong>Session ID</strong> &mdash; a PHP session cookie used for authentication and CSRF protection; also stored in search logs alongside your account ID</li>
      <li><strong>Browser and device information</strong> &mdash; User-Agent headers recorded in server logs for debugging</li>
      <li><strong>Cloudflare Turnstile challenge token</strong> &mdash; generated by the Turnstile widget on login, registration, forgot-password, and reset-password forms for bot detection</li>
      <li><strong>CSP violation reports</strong> &mdash; automatically sent by your browser when a Content Security Policy violation occurs; logged with IP address, violated directive, blocked URI, and source file</li>
      <li><strong>Timestamps</strong> &mdash; recorded for every account action (login, borrow, rating, etc.) for record-keeping and session timeout enforcement</li>
    </ul>

    <h3>Information from Third Parties</h3>
    <ul>
      <li><strong>Google Geocoding API</strong> &mdash; latitude and longitude for your address, used solely to resolve your neighborhood ID; coordinates are not stored on your account record</li>
      <li><strong>Stripe</strong> &mdash; payment confirmation and Payment Intent ID when a security deposit is processed</li>
    </ul>
