  <section id="how-we-use">
    <h2>How We Use Your Information</h2>
    <ol>
      <li><strong>Account creation and authentication</strong> &mdash; name, username, email, password hash, ZIP code, and neighborhood to create and secure your account</li>
      <li><strong>Profile display</strong> &mdash; first name, last name, username, bio, avatar, neighborhood, and member-since date are shown publicly on your profile; email and street address are never displayed publicly</li>
      <li><strong>Tool lending and borrowing</strong> &mdash; tool details, borrow requests, loan duration, notes, pickup/return codes, and deposit amounts to facilitate transactions between neighbors</li>
      <li><strong>Payments and deposits</strong> &mdash; Stripe processes card payments for security deposits; we store only the payment confirmation ID and amount, never your card number</li>
      <li><strong>Communication</strong> &mdash; in-app notifications for borrow requests, approvals, denials, due-date reminders, return confirmations, and rating requests; email is used only for password reset links (sent to the address you registered with)</li>
      <li><strong>Ratings and reputation</strong> &mdash; aggregated scores displayed publicly to help neighbors make informed lending and borrowing decisions; individual review text is attributed to the reviewer&rsquo;s first name</li>
      <li><strong>Search functionality</strong> &mdash; search queries logged with account ID (if logged in), IP address, and session ID to improve search results and monitor for abuse</li>
      <li><strong>Security and abuse prevention</strong> &mdash; IP address, session data, CSRF tokens, rate-limiting counters, honeypot fields, Cloudflare Turnstile challenge tokens, and CSP violation reports to protect against bots, brute-force attacks, and unauthorized access</li>
      <li><strong>Geolocation</strong> &mdash; street address sent to Google Geocoding API solely to resolve your neighborhood; coordinates are not stored on your account record</li>
      <li><strong>Dispute resolution</strong> &mdash; incident reports, dispute messages, and admin notes to mediate issues between lenders and borrowers</li>
      <li><strong>Legal compliance</strong> &mdash; TOS acceptance records (account ID, version, timestamp) to document consent</li>
      <li><strong>Platform administration</strong> &mdash; aggregated statistics (member count, tool count, active borrows) for site operation; admins can view individual account details to enforce community guidelines</li>
    </ol>
  </section>

  <section id="how-we-share">
    <h2>How We Share Your Information</h2>
    <p><strong>We do not sell your personal data. We do not share it with advertisers.</strong></p>
    <p>We share information only in the following limited circumstances:</p>
    <ul>
      <li><strong>Other NeighborhoodTools members</strong> &mdash; first name, last name, neighborhood, bio, avatar, tool listings, and ratings, as required for the lending and borrowing platform to function</li>
      <li><strong>Cloudflare (Turnstile)</strong> &mdash; IP address, browser signals, and challenge token for bot detection on authentication forms; Cloudflare acts as a data processor and uses this data only to provide the Turnstile anti-bot service</li>
      <li><strong>Google (Geocoding API)</strong> &mdash; street address and ZIP code for one-time neighborhood resolution during registration; Google&rsquo;s Cloud API Terms of Service apply</li>
      <li><strong>Stripe</strong> &mdash; payment amount, currency, and deposit/account metadata for deposit processing; Stripe acts as a data processor; card details are entered directly into Stripe&rsquo;s payment element and never touch our server</li>
      <li><strong>Law enforcement</strong> &mdash; any data, if legally compelled, in response to valid legal process (subpoena, court order, or equivalent)</li>
    </ul>
  </section>

  <section id="cookies">
    <h2>Cookies and Session Data</h2>
    <p>NeighborhoodTools uses a single essential cookie:</p>
    <ul>
      <li><strong>PHPSESSID</strong> (session cookie) &mdash; maintains your login state and CSRF protection. Expires when you close your browser or after 30 minutes of inactivity. Flags: <code>HttpOnly</code>, <code>Secure</code>, <code>SameSite=Lax</code>.</li>
    </ul>
    <p>Cloudflare Turnstile may set a short-lived challenge-clearance cookie (<strong>cf_clearance</strong>) on authentication pages, lasting up to 30 minutes.</p>
    <p>We do not use:</p>
    <ul>
      <li>Analytics cookies (no Google Analytics, Mixpanel, Hotjar, etc.)</li>
      <li>Advertising or tracking cookies</li>
      <li>Third-party social media cookies</li>
      <li>Persistent first-party cookies beyond the session cookie</li>
    </ul>
  </section>

  <section id="data-retention">
    <h2>How Long We Keep Your Information</h2>
    <ul>
      <li><strong>Account data</strong> (name, email, profile) &mdash; retained while your account is active; soft-deleted accounts are marked inactive but records are preserved for dispute and transaction history integrity</li>
      <li><strong>Password hash</strong> &mdash; retained until changed by you or your account is deleted</li>
      <li><strong>Password reset tokens</strong> &mdash; expire after 1 hour; consumed on use</li>
      <li><strong>Session data</strong> &mdash; deleted after 30 minutes of inactivity</li>
      <li><strong>Rate-limiting records</strong> &mdash; automatically purged after the rate window expires (15 minutes to 1 hour depending on the action)</li>
      <li><strong>Borrow and transaction history</strong> &mdash; retained indefinitely for record-keeping and dispute resolution</li>
      <li><strong>Ratings and reviews</strong> &mdash; retained indefinitely as part of the public reputation system</li>
      <li><strong>Search logs</strong> &mdash; approximately 30 days; purged weekly by automated cleanup</li>
      <li><strong>Tool and profile photos</strong> &mdash; retained while the tool or profile exists; old images are deleted when replaced</li>
      <li><strong>Incident reports and photos</strong> &mdash; retained indefinitely for platform safety records</li>
      <li><strong>TOS acceptance records</strong> &mdash; retained indefinitely for compliance documentation</li>
      <li><strong>CSP violation logs</strong> &mdash; retained per server log configuration</li>
      <li><strong>Server error logs</strong> &mdash; retained per server configuration (not controlled by the application)</li>
    </ul>
  </section>

  <section id="data-security">
    <h2>How We Protect Your Information</h2>
    <p>We take the security of your data seriously and implement industry-standard safeguards, including:</p>
    <ul>
      <li><strong>Encryption</strong> &mdash; all connections are encrypted in transit; passwords are stored using a strong one-way hash and are never retained in plaintext</li>
      <li><strong>Session protection</strong> &mdash; sessions expire after periods of inactivity and are secured against common web-based attacks</li>
      <li><strong>Input and output safeguards</strong> &mdash; all user-supplied data is validated on the server and sanitized before display to prevent injection and scripting attacks</li>
      <li><strong>Rate limiting</strong> &mdash; automated safeguards are in place to prevent brute-force and abuse attempts</li>
      <li><strong>Bot protection</strong> &mdash; authentication forms are protected by Cloudflare Turnstile to distinguish real users from automated scripts</li>
      <li><strong>Upload validation</strong> &mdash; uploaded files are verified by type and size before being accepted</li>
      <li><strong>Access controls</strong> &mdash; role-based permissions ensure users can only access data and actions appropriate to their account</li>
      <li><strong>Security headers</strong> &mdash; the platform sends browser-level directives that reduce exposure to common attack vectors</li>
    </ul>
    <p>No system is completely secure. If you believe your account has been compromised, please <a href="mailto:contact@neighborhoodtools.org">contact us</a> immediately.</p>
  </section>

